public class ConditionalExpressions {
    public static void main(String args[]) {
        int a = 10;
        int b = 4;
        boolean c = a == b;         // false
        boolean d = a == 10;       // true

        int a1 = 10;
        int b1 = 4;
        boolean c1 = a != b;         // true
        boolean d1 = a != 10;       // false
    }
}
